public class Algebra
{
    public decimal Addition(decimal x = 0, decimal y = 0)
    {
        return x + y;
    }

    public decimal Subtraction(decimal x = 0, decimal y = 0)
    {
        return x - y;
    }

    public decimal Multiplication(decimal x = 0, decimal y = 0)
    {
        return x * y;
    }

    public decimal Division(decimal x = 0, decimal y = 1)
    {
        return x / y;
    }
}